    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //bouton connection
        try{
            Connect();
            pst=(PreparedStatement) con.prepareStatement("select * from login where nom=? and mdp=?");
            pst.setString(1, txtnom.getText());
            pst.setString(2, txtmdp.getText());
            rs=pst.executeQuery();
            
            //conditions 
            if (rs.next()) {
                //JOptionPane.showMessageDialog(null, "Connection établie");
                MenuePrincipal me=new MenuePrincipal();
                me.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Erreur de Connection");
                txtnom.setText("");
                txtmdp.setText("");
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed
